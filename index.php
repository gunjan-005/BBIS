    <!-- 1. Eligibility Criteria -->
    <section class="card" id="eligibility">
        <h2>Eligibility Criteria</h2>

        <h3>Who Can Apply</h3>
        <ul>
            <li>Applicants who have completed 10+2 (Intermediate / equivalent) from a board/university recognized by Kathmandu University.</li>
            <li>Open to all streams: Science, Management, Humanities, Law, etc.</li>
        </ul>
<br>
        <h3>Foreign Systems / Alternate Credentials</h3>
        <ul>
            <li><strong>GCE A-Level:</strong> Must have passed at least 3 subjects in A-Level + 1 general paper at AS-Level (as per KU guidelines).</li>
            <li><strong>IB (International Baccalaureate):</strong> Must pass all 6 courses with at least Grade 4 each and approx. 28 aggregate marks (depending on cycle).</li>
        </ul>
<br>
        <h3>Minimum Grade / Aggregate Requirement</h3>
        <ul>
            <li>At least <strong>50% aggregate</strong> or <strong>CGPA 2.0</strong> on a 4.0 scale AND at least Grade ‘C’ in each subject.</li>
            <li>Older cycles sometimes allowed <strong>40% / CGPA 1.6</strong> with ‘D+’ minimum.</li>
        </ul>
<br>
        <h3>Nationality</h3>
        <p>Open to both Nepali and non-Nepali nationals.</p>

        <h3>Other Requirements</h3>
        <p>No requirement of math or computer science background the program accepts any academic discipline.</p>

        <p><em>Note: There is no age limit or work experience requirement.</em></p>
    </section>

